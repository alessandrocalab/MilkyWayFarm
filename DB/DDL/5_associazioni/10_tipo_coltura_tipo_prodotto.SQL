CREATE TABLE TIPO_COLTURA_TIPO_PRODOTTO(
    NOME_TIPO_COLTURA VARCHAR(60),
    NOME_PRODOTTO VARCHAR(60),
    QUANTITA_STIMATE_PER_KG_SEMI NUMBER(7,3),

    --PK
    CONSTRAINT PK_TIPO_COLTURA_TIPO_PRODOTTO
        PRIMARY KEY (NOME_TIPO_COLTURA, NOME_PRODOTTO),

    --FK
    CONSTRAINT FK_TIPO_COLTURA_TIPO_PRODOTTO_TIPO_COLTURA
        FOREIGN KEY (NOME_TIPO_COLTURA)
        REFERENCES TIPO_COLTURA (NOME_TIPO_COLTURA),
    
    CONSTRAINT FK_TIPO_COLTURA_TIPO_PRODOTTO_TIPO_PRODOTTO
        FOREIGN KEY (NOME_PRODOTTO)
        REFERENCES TIPO_PRODOTTO (NOME_PRODOTTO),
    
    --CK
    CONSTRAINT CK_TIPO_COLTURA_TIPO_PRODOTTO_QUANTITA_STIMATE_PER_KG_SEMI
        CHECK (QUANTITA_STIMATE_PER_KG_SEMI>0)
)