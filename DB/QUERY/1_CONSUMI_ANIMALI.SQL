
SELECT DCP.NOME_PRODOTTO, SUM(DCP.QUANTITA) AS QUANTITA_CONSUMATA
FROM DIETA_COMPRENDE_PRODOTTO DCP
JOIN 
( --CON QUESTA TABELLA PRENDIAMO IN UN DETERMINATO GIORNO
  --TUTTE LE DIETE SERVITE
    SELECT 
        DA.NOME_DIETA
    FROM ANIMALE ANM 


    JOIN STADIO_CRESCITA SC 
    ON SC.NOME_TIPO_ANIMALE=ANM.NOME_TIPO_ANIMALE
    AND SC.ETA_MINIMA_MESI=(
        SELECT MAX(SC2.ETA_MINIMA_MESI)
        FROM STADIO_CRESCITA SC2
        WHERE SC2.NOME_TIPO_ANIMALE=ANM.NOME_TIPO_ANIMALE
        AND SC2.ETA_MINIMA_MESI <=((EXTRACT(YEAR FROM(DATE '2025-05-12')))-ANM.ANNO_NASCITA)*12 
        + ((EXTRACT(MONTH FROM(DATE '2025-05-12')))-ANM.MESE_NASCITA)
    )

    JOIN( SELECT *
        FROM STADIO_CRESCITA_PREVEDE_DIETA SCPD 
        WHERE SCPD.DATA_INIZIO <= DATE '2025-05-12'
        AND(
            SCPD.DATA_FINE IS NULL
            OR SCPD.DATA_FINE > DATE '2025-05-12'
        )
    ) DA --DIETA ATTIVA
    ON DA.NOME_STADIO_CRESCITA=SC.NOME_STADIO_CRESCITA
    AND DA.NOME_TIPO_ANIMALE=ANM.NOME_TIPO_ANIMALE

    WHERE ETICHETTA IN (
        SELECT ETICHETTA_ANIMALE
        FROM ANIMALE_ALLOCATO_BLOCCO AAB
        WHERE AAB.DATA_ALLOCAZIONE <= DATE '2025-05-12'
        AND(
            AAB.DATA_DEALLOCAZIONE IS NULL
            OR AAB.DATA_DEALLOCAZIONE > DATE '2025-05-12'
        )
    )
) DS --DIETE SERVITE

ON DS.NOME_DIETA=DCP.NOME_DIETA

GROUP BY NOME_PRODOTTO

UNION ALL

SELECT PA.NOME_PRODOTTO, PA.QUANTITA_GIORNALIERA AS QUANTITA_CONSUMATA
FROM PRESCRIZIONE_ANIMALE PA 
WHERE PA.DATA_VISITA <= DATE '2025-05-12'
AND (PA.DATA_VISITA+PA.DURATA_GIORNI) >= DATE '2025-05-12';