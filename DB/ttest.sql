SELECT DATA_MONTAGGIO, DATA_SMONTAGGIO, CODICE_CELLA_IDR, NOME_STRUTTURA
            FROM CELLA_IDR_CONTIENE_SENSORE CICS
            WHERE CICS.SERIALE='SENS-000A-SA-ENERGY-01'
            AND CICS.NOME_PRODUTTORE='MESA'
            AND CICS.NOME_MODELLO='ENERGY-SENSOR-V3'

            UNION 

            SELECT DATA_MONTAGGIO, DATA_SMONTAGGIO, NUMERO_BLOCCO, NOME_STRUTTURA
            FROM BLOCCO_ANIMALE_CONTIENE_SENSORE BACS
            WHERE BACS.SERIALE='SENS-000A-SA-ENERGY-01'
            AND BACS.NOME_PRODUTTORE='MESA'
            AND BACS.NOME_MODELLO='ENERGY-SENSOR-V3'

