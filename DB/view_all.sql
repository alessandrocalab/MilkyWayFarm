--TENGO TRACCIA DI TUTTI I CONSUMI DEI CICLI DI COLTIVAZIONE

SELECT A.ETICHETTA, SC.NOME_STADIO_CRESCITA, 
A.NOME_TIPO_ANIMALE, ((((EXTRACT(YEAR FROM SYSDATE))-A.ANNO_NASCITA)*12)+
      ((EXTRACT(MONTH FROM SYSDATE)-A.MESE_NASCITA))) AS ETA
FROM ANIMALE A
JOIN STADIO_CRESCITA SC ON SC.NOME_TIPO_ANIMALE=A.NOME_TIPO_ANIMALE 
WHERE ((((EXTRACT(YEAR FROM SYSDATE))-A.ANNO_NASCITA)*12)+
      ((EXTRACT(MONTH FROM SYSDATE)-A.MESE_NASCITA))) >= SC.ETA_MINIMA_MESI

