CREATE OR REPLACE PACKAGE BODY SIMULATORE AS
    SUBTYPE T_NOME IS VARCHAR(60);

    FUNCTION RANDOM_NUMBER(
        P_MIN IN NUMBER,
        P_MAX IN NUMBER
    ) RETURN NUMBER IS
    BEGIN
        RETURN DBMS_RANDOM.VALUE(P_MIN, P_MAX);
    END RANDOM_NUMBER;

    FUNCTION RANDOM_INT(
        P_MIN IN PLS_INTEGER,
        P_MAX IN PLS_INTEGER
    ) RETURN PLS_INTEGER IS
    BEGIN
        RETURN TRUNC(DBMS_RANDOM.VALUE(P_MIN, P_MAX + 1));
    END RANDOM_INT;

    FUNCTION RANDOM_TRIANGULAR(
        P_MIN IN NUMBER,
        P_MAX IN NUMBER,
        P_MODE IN NUMBER
    ) RETURN NUMBER IS
        V_U NUMBER := DBMS_RANDOM.VALUE(0, 1);
        V_C NUMBER := (P_MODE - P_MIN) / (P_MAX - P_MIN);
    BEGIN
        IF V_U < V_C THEN
            RETURN P_MIN + SQRT(V_U * (P_MAX - P_MIN) * (P_MODE - P_MIN));
        END IF;

        RETURN P_MAX - SQRT((1 - V_U) * (P_MAX - P_MIN) * (P_MAX - P_MODE));
    END RANDOM_TRIANGULAR;

    FUNCTION IS_CELLA_WORKING(
        P_CODICE_CELLA_IDR IN CELLA_IDROPONICA.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CELLA_IDROPONICA.NOME_STRUTTURA%TYPE
    ) RETURN BOOLEAN IS
        V_COUNT NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_COUNT
        FROM CICLO_COLTIVAZIONE CC
        WHERE CC.DATA_FINE_EFFETTIVA IS NULL
        AND CC.CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
        AND CC.NOME_STRUTTURA = P_NOME_STRUTTURA;

        RETURN V_COUNT > 0;
    END IS_CELLA_WORKING;

    FUNCTION GET_SIM_START_DAY RETURN DATE IS
        V_MIN_ANIMALE DATE;
        V_MIN_CELLA DATE;
    BEGIN
        SELECT MIN(DATA_ALLOCAZIONE)
        INTO V_MIN_ANIMALE
        FROM ANIMALE_ALLOCATO_BLOCCO;

        SELECT MIN(DATA_MONTAGGIO)
        INTO V_MIN_CELLA
        FROM CELLA_IDROPONICA;

        IF V_MIN_ANIMALE IS NULL THEN
            RETURN V_MIN_CELLA;
        ELSIF V_MIN_CELLA IS NULL THEN
            RETURN V_MIN_ANIMALE;
        END IF;

        RETURN LEAST(V_MIN_ANIMALE, V_MIN_CELLA);
    END GET_SIM_START_DAY;

    FUNCTION GET_CYCLE_DURATION(
        P_DATA_INIZIO IN CICLO_COLTIVAZIONE.DATA_INIZIO%TYPE,
        P_CODICE_CELLA_IDR IN CICLO_COLTIVAZIONE.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CICLO_COLTIVAZIONE.NOME_STRUTTURA%TYPE,
        P_NOME_TIPO_COLTURA IN CICLO_COLTIVAZIONE.NOME_TIPO_COLTURA%TYPE
    ) RETURN NUMBER IS
        V_DURATA NUMBER;
    BEGIN
        BEGIN
            SELECT MAX(TC.DURATA_CICLO_COLTURA_GIORNI)
            INTO V_DURATA
            FROM TIPO_COLTURA TC
            WHERE TC.NOME_SEMI IN (
                SELECT CCSM.NOME_SEMI
                FROM CICLO_COLT_UTILIZZA_SEMI_MISSIONE CCSM
                WHERE CCSM.DATA_INIZIO = P_DATA_INIZIO
                AND CCSM.CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
                AND CCSM.NOME_STRUTTURA = P_NOME_STRUTTURA

                UNION

                SELECT CCPA.NOME_PRODOTTO
                FROM CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA CCPA
                WHERE CCPA.DATA_INIZIO = P_DATA_INIZIO
                AND CCPA.CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
                AND CCPA.NOME_STRUTTURA = P_NOME_STRUTTURA
            );
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                V_DURATA := NULL;
        END;

        IF V_DURATA IS NULL THEN
            SELECT TC.DURATA_CICLO_COLTURA_GIORNI
            INTO V_DURATA
            FROM TIPO_COLTURA TC
            WHERE TC.NOME_TIPO_COLTURA = P_NOME_TIPO_COLTURA;
        END IF;

        RETURN V_DURATA;
    END GET_CYCLE_DURATION;

    FUNCTION GET_PLANTED_SEED_QUANTITY(
        P_DATA_INIZIO IN CICLO_COLTIVAZIONE.DATA_INIZIO%TYPE,
        P_CODICE_CELLA_IDR IN CICLO_COLTIVAZIONE.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CICLO_COLTIVAZIONE.NOME_STRUTTURA%TYPE
    ) RETURN NUMBER IS
        V_QUANTITA NUMBER;
    BEGIN
        SELECT NVL(SUM(Q), 0)
        INTO V_QUANTITA
        FROM (
            SELECT CCSM.QUANTITA AS Q
            FROM CICLO_COLT_UTILIZZA_SEMI_MISSIONE CCSM
            WHERE CCSM.DATA_INIZIO = P_DATA_INIZIO
            AND CCSM.CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
            AND CCSM.NOME_STRUTTURA = P_NOME_STRUTTURA

            UNION ALL

            SELECT CCPA.QUANTITA AS Q
            FROM CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA CCPA
            WHERE CCPA.DATA_INIZIO = P_DATA_INIZIO
            AND CCPA.CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
            AND CCPA.NOME_STRUTTURA = P_NOME_STRUTTURA
        );

        RETURN V_QUANTITA;
    END GET_PLANTED_SEED_QUANTITY;

    FUNCTION END_CYCLE_PROB(
        P_DAYS_FROM_START IN NUMBER,
        P_EXPECTED_DAYS IN NUMBER
    ) RETURN BOOLEAN IS
        V_MAX_DAYS PLS_INTEGER := GREATEST(P_DAYS_FROM_START, P_EXPECTED_DAYS);
        V_MIN_DAYS PLS_INTEGER := LEAST(P_DAYS_FROM_START, P_EXPECTED_DAYS);
        V_N PLS_INTEGER;
    BEGIN
        V_N := RANDOM_INT(V_MIN_DAYS, V_MAX_DAYS);

        IF V_N = V_MAX_DAYS THEN
            RETURN TRUE;
        END IF;

        IF V_MAX_DAYS != P_EXPECTED_DAYS THEN
            RETURN RANDOM_INT(1, 10) = 10;
        END IF;

        RETURN FALSE;
    END END_CYCLE_PROB;

    FUNCTION IS_CYCLE_FAILED(
        P_DAYS_FROM_START IN NUMBER,
        P_EXPECTED_DAYS IN NUMBER
    ) RETURN BOOLEAN IS
    BEGIN
        RETURN P_DAYS_FROM_START < (P_EXPECTED_DAYS * 0.8);
    END IS_CYCLE_FAILED;

    FUNCTION IS_REUSABLE_SEED_PRODUCT(
        P_NOME_PRODOTTO IN T_NOME
    ) RETURN BOOLEAN IS
        V_COUNT NUMBER;
    BEGIN
        SELECT COUNT(*)
        INTO V_COUNT
        FROM TIPO_COLTURA TC
        WHERE TC.NOME_SEMI = P_NOME_PRODOTTO;

        RETURN V_COUNT > 0;
    END IS_REUSABLE_SEED_PRODUCT;

    FUNCTION GET_TIPO_COLTURA_BY_SEED(
        P_NOME_SEMI IN T_NOME
    ) RETURN T_NOME IS
        V_NOME_TIPO_COLTURA T_NOME;
    BEGIN
        SELECT NOME_TIPO_COLTURA
        INTO V_NOME_TIPO_COLTURA
        FROM (
            SELECT TC.NOME_TIPO_COLTURA
            FROM TIPO_COLTURA TC
            WHERE UPPER(TC.NOME_SEMI) = UPPER(P_NOME_SEMI)
            ORDER BY DBMS_RANDOM.VALUE
        )
        WHERE ROWNUM = 1;

        RETURN V_NOME_TIPO_COLTURA;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RETURN NULL;
    END GET_TIPO_COLTURA_BY_SEED;

    FUNCTION GET_MOD_COLT_BY_TIPO_COLTURA(
        P_NOME_TIPO_COLTURA IN T_NOME
    ) RETURN T_NOME IS
        V_NOME_MODALITA T_NOME;
    BEGIN
        SELECT NOME_MODALITA_COLTIVAZIONE
        INTO V_NOME_MODALITA
        FROM (
            SELECT TCMC.NOME_MODALITA_COLTIVAZIONE
            FROM TIPO_COLT_ACCETTA_MOD_COLT TCMC
            WHERE UPPER(TCMC.NOME_TIPO_COLTURA) = UPPER(P_NOME_TIPO_COLTURA)
            ORDER BY DBMS_RANDOM.VALUE
        )
        WHERE ROWNUM = 1;

        RETURN V_NOME_MODALITA;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            RETURN NULL;
    END GET_MOD_COLT_BY_TIPO_COLTURA;

    PROCEDURE INSERT_COLTIVATION_CYCLE(
        P_DATA_INIZIO IN CICLO_COLTIVAZIONE.DATA_INIZIO%TYPE,
        P_CODICE_CELLA_IDR IN CICLO_COLTIVAZIONE.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CICLO_COLTIVAZIONE.NOME_STRUTTURA%TYPE,
        P_NOME_MOD_COLTIVAZIONE IN CICLO_COLTIVAZIONE.NOME_MOD_COLTIVAZIONE%TYPE,
        P_NOME_TIPO_COLTURA IN CICLO_COLTIVAZIONE.NOME_TIPO_COLTURA%TYPE
    ) IS
    BEGIN
        INSERT INTO CICLO_COLTIVAZIONE (
            DATA_INIZIO,
            CODICE_CELLA_IDR,
            NOME_STRUTTURA,
            DATA_FINE_EFFETTIVA,
            NOME_MOD_COLTIVAZIONE,
            NOME_TIPO_COLTURA
        ) VALUES (
            P_DATA_INIZIO,
            P_CODICE_CELLA_IDR,
            P_NOME_STRUTTURA,
            NULL,
            P_NOME_MOD_COLTIVAZIONE,
            P_NOME_TIPO_COLTURA
        );
    END INSERT_COLTIVATION_CYCLE;

    PROCEDURE INSERT_CYCLE_USE_MISSION_SEED(
