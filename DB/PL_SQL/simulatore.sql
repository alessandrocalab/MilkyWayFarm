    PROCEDURE INSERT_AGRI_PRODUCTION(
        P_DATA_PRODUZIONE IN PRODUZIONE_AGRICOLA.DATA_PRODUZIONE_AGRICOLA%TYPE,
        P_DATA_INIZIO_CICLO IN PRODUZIONE_AGRICOLA.DATA_INIZIO_CICLO_COLTIVAZIONE%TYPE,
        P_CODICE_CELLA_IDR IN PRODUZIONE_AGRICOLA.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN PRODUZIONE_AGRICOLA.NOME_STRUTTURA%TYPE,
        P_NOME_PRODOTTO IN PRODUZIONE_AGRICOLA.NOME_PRODOTTO%TYPE,
        P_QUANTITA IN PRODUZIONE_AGRICOLA.QUANTITA%TYPE
    ) IS
    BEGIN
        INSERT INTO PRODUZIONE_AGRICOLA (
            DATA_PRODUZIONE_AGRICOLA,
            DATA_INIZIO_CICLO_COLTIVAZIONE,
            CODICE_CELLA_IDR,
            NOME_STRUTTURA,
