            P_NOME_STRUTTURA,
            P_NOME_PRODOTTO,
            ROUND(P_QUANTITA, 2)
        );
    END INSERT_AGRI_PRODUCTION;

    PROCEDURE END_CYCLE(
        P_DATA_INIZIO IN CICLO_COLTIVAZIONE.DATA_INIZIO%TYPE,
        P_CODICE_CELLA_IDR IN CICLO_COLTIVAZIONE.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CICLO_COLTIVAZIONE.NOME_STRUTTURA%TYPE,
        P_DATA_FINE IN CICLO_COLTIVAZIONE.DATA_FINE_EFFETTIVA%TYPE
    ) IS
    BEGIN
        UPDATE CICLO_COLTIVAZIONE
        SET DATA_FINE_EFFETTIVA = P_DATA_FINE
        WHERE DATA_INIZIO = P_DATA_INIZIO
        AND CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
        AND NOME_STRUTTURA = P_NOME_STRUTTURA;
    END END_CYCLE;

    PROCEDURE START_CICLO_COLTIVAZIONE(
        P_CODICE_CELLA_IDR IN CELLA_IDROPONICA.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CELLA_IDROPONICA.NOME_STRUTTURA%TYPE,
        P_DATA IN DATE
    ) IS
        V_NOME_SEMI T_NOME;
        V_DATA_ARRIVO_SEMI DATE;
        V_DATA_PRODUZIONE_AGRICOLA DATE;
        V_DATA_INIZIO_CICLO_COLT DATE;
        V_CODICE_CELLA_IDR_PROD CELLA_IDROPONICA.CODICE_CELLA_IDR%TYPE;
        V_NOME_STRUTTURA_PROD CELLA_IDROPONICA.NOME_STRUTTURA%TYPE;
        V_QUANTITA_RESIDUA NUMBER;
        V_TO_PLANT NUMBER;
        V_NOME_TIPO_COLTURA T_NOME;
        V_NOME_MODALITA T_NOME;
        V_USE_AGRI_PRODUCTION BOOLEAN := FALSE;
    BEGIN
        IF RANDOM_INT(1, 3) != 3 THEN
            RETURN;
        END IF;

        BEGIN
            SELECT
                DATA_PRODUZIONE_AGRICOLA,
                DATA_INIZIO_CICLO_COLTIVAZIONE,
                CODICE_CELLA_IDR,
                NOME_STRUTTURA,
                NOME_PRODOTTO,
                QUANTITA_RESIDUA
            INTO
                V_DATA_PRODUZIONE_AGRICOLA,
                V_DATA_INIZIO_CICLO_COLT,
                V_CODICE_CELLA_IDR_PROD,
                V_NOME_STRUTTURA_PROD,
                V_NOME_SEMI,
                V_QUANTITA_RESIDUA
            FROM (
                SELECT
                    DATA_PRODUZIONE_AGRICOLA,
                    DATA_INIZIO_CICLO_COLTIVAZIONE,
                    CODICE_CELLA_IDR,
                    NOME_STRUTTURA,
                    NOME_PRODOTTO,
                    QUANTITA_RESIDUA
                FROM (
                    SELECT
                        PA.DATA_PRODUZIONE_AGRICOLA,
                        PA.DATA_INIZIO_CICLO_COLTIVAZIONE,
                        PA.CODICE_CELLA_IDR,
                        PA.NOME_STRUTTURA,
                        PA.NOME_PRODOTTO,
                        PA.QUANTITA - NVL((
                            SELECT SUM(CCPA.QUANTITA)
                            FROM CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA CCPA
                            WHERE CCPA.DATA_PRODUZIONE_AGRICOLA = PA.DATA_PRODUZIONE_AGRICOLA
                            AND CCPA.DATA_INIZIO_CICLO_COLTIVAZIONE = PA.DATA_INIZIO_CICLO_COLTIVAZIONE
                            AND CCPA.CODICE_CELLA_IDR_PROD_AGR = PA.CODICE_CELLA_IDR
                            AND CCPA.NOME_STRUTTURA_PROD_AGR = PA.NOME_STRUTTURA
                            AND CCPA.NOME_PRODOTTO = PA.NOME_PRODOTTO
                        ), 0) AS QUANTITA_RESIDUA
                    FROM PRODUZIONE_AGRICOLA PA
                    WHERE PA.NOME_PRODOTTO IN (
                        SELECT TC.NOME_SEMI
                        FROM TIPO_COLTURA TC
                    )
                )
                WHERE QUANTITA_RESIDUA >= 2
                ORDER BY DBMS_RANDOM.VALUE
            )
            WHERE ROWNUM = 1;

            V_USE_AGRI_PRODUCTION := TRUE;
        EXCEPTION
            WHEN NO_DATA_FOUND THEN
                V_USE_AGRI_PRODUCTION := FALSE;
        END;

        IF NOT V_USE_AGRI_PRODUCTION THEN
            BEGIN
                SELECT NOME_SEMI, DATA_ARRIVO_SEMI, QUANTITA_RESIDUA
                INTO V_NOME_SEMI, V_DATA_ARRIVO_SEMI, V_QUANTITA_RESIDUA
                FROM (
                    SELECT
                        NOME_SEMI,
                        DATA_ARRIVO_SEMI,
                        QUANTITA_RESIDUA
                    FROM (
                        SELECT
                            SM.NOME_SEMI,
                            SM.DATA_ARRIVO_SEMI,
                            SM.QUANTITA_SEMI - NVL((
                                SELECT SUM(CCSM.QUANTITA)
                                FROM CICLO_COLT_UTILIZZA_SEMI_MISSIONE CCSM
                                WHERE CCSM.DATA_ARRIVO_SEMI = SM.DATA_ARRIVO_SEMI
                                AND CCSM.NOME_SEMI = SM.NOME_SEMI
                            ), 0) AS QUANTITA_RESIDUA
                        FROM SEMI_MISSIONE SM
                    )
                    WHERE QUANTITA_RESIDUA >= 2
                    ORDER BY DBMS_RANDOM.VALUE
                )
                WHERE ROWNUM = 1;
            EXCEPTION
                WHEN NO_DATA_FOUND THEN
                    DBMS_OUTPUT.PUT_LINE('No available seeds');
                    RETURN;
            END;
        END IF;

        V_NOME_TIPO_COLTURA := GET_TIPO_COLTURA_BY_SEED(V_NOME_SEMI);
        IF V_NOME_TIPO_COLTURA IS NULL THEN
            RETURN;
        END IF;

        V_NOME_MODALITA := GET_MOD_COLT_BY_TIPO_COLTURA(V_NOME_TIPO_COLTURA);
        IF V_NOME_MODALITA IS NULL THEN
            RETURN;
        END IF;

        V_TO_PLANT := ROUND(RANDOM_NUMBER(0.5, LEAST(V_QUANTITA_RESIDUA, 3)), 3);

        INSERT_COLTIVATION_CYCLE(
            TRUNC(P_DATA),
            P_CODICE_CELLA_IDR,
            P_NOME_STRUTTURA,
            V_NOME_MODALITA,
            V_NOME_TIPO_COLTURA
        );

        IF V_USE_AGRI_PRODUCTION THEN
            INSERT_CYCLE_USE_AGRI_PRODUCTION(
                TRUNC(P_DATA),
                P_CODICE_CELLA_IDR,
                P_NOME_STRUTTURA,
                V_DATA_PRODUZIONE_AGRICOLA,
                V_DATA_INIZIO_CICLO_COLT,
                V_CODICE_CELLA_IDR_PROD,
                V_NOME_STRUTTURA_PROD,
                V_NOME_SEMI,
                V_TO_PLANT
            );
        ELSE
            INSERT_CYCLE_USE_MISSION_SEED(
                TRUNC(P_DATA),
                P_CODICE_CELLA_IDR,
                P_NOME_STRUTTURA,
                V_NOME_SEMI,
                V_DATA_ARRIVO_SEMI,
                V_TO_PLANT
            );
        END IF;

        DBMS_OUTPUT.PUT_LINE(
            'Started a new coltivation cycle in cell: ' ||
            P_CODICE_CELLA_IDR || ' ' || P_NOME_STRUTTURA
        );
    END START_CICLO_COLTIVAZIONE;

    PROCEDURE SIMULA_CELLA(
        P_CODICE_CELLA_IDR IN CELLA_IDROPONICA.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CELLA_IDROPONICA.NOME_STRUTTURA%TYPE,
        P_DATA IN DATE
    ) IS
        V_DATA_INIZIO CICLO_COLTIVAZIONE.DATA_INIZIO%TYPE;
        V_NOME_TIPO_COLTURA CICLO_COLTIVAZIONE.NOME_TIPO_COLTURA%TYPE;
        V_DURATA NUMBER;
        V_DAYS_FROM_START NUMBER;
        V_PLANTED_QUANTITY NUMBER;
        V_PRODUCTED NUMBER;
    BEGIN
        IF NOT IS_CELLA_WORKING(P_CODICE_CELLA_IDR, P_NOME_STRUTTURA) THEN
            START_CICLO_COLTIVAZIONE(P_CODICE_CELLA_IDR, P_NOME_STRUTTURA, P_DATA);
            RETURN;
        END IF;

        SELECT DATA_INIZIO, NOME_TIPO_COLTURA
        INTO V_DATA_INIZIO, V_NOME_TIPO_COLTURA
        FROM (
            SELECT CC.DATA_INIZIO, CC.NOME_TIPO_COLTURA
            FROM CICLO_COLTIVAZIONE CC
            WHERE CC.CODICE_CELLA_IDR = P_CODICE_CELLA_IDR
            AND CC.NOME_STRUTTURA = P_NOME_STRUTTURA
            ORDER BY CC.DATA_INIZIO DESC
        )
        WHERE ROWNUM = 1;

        V_DURATA := GET_CYCLE_DURATION(
            V_DATA_INIZIO,
            P_CODICE_CELLA_IDR,
            P_NOME_STRUTTURA,
            V_NOME_TIPO_COLTURA
        );
        V_DAYS_FROM_START := TRUNC(P_DATA) - TRUNC(V_DATA_INIZIO);

        DBMS_OUTPUT.PUT_LINE(
            'Cell ' || P_NOME_STRUTTURA || ' ' || P_CODICE_CELLA_IDR ||
            ' is now working from ' || V_DAYS_FROM_START || '/' || V_DURATA
        );

        IF END_CYCLE_PROB(V_DAYS_FROM_START, V_DURATA) THEN
            V_PLANTED_QUANTITY := GET_PLANTED_SEED_QUANTITY(
                V_DATA_INIZIO,
                P_CODICE_CELLA_IDR,
                P_NOME_STRUTTURA
            );

            IF NOT IS_CYCLE_FAILED(V_DAYS_FROM_START, V_DURATA) THEN
                FOR PROD IN (
                    SELECT
                        NOME_PRODOTTO,
                        QUANTITA_STIMATE_PER_UNITA_SEMI
                    FROM TIPO_COLTURA_TIPO_PRODOTTO
                    WHERE NOME_TIPO_COLTURA = V_NOME_TIPO_COLTURA
                ) LOOP
                    V_PRODUCTED :=
                        V_PLANTED_QUANTITY *
                        PROD.QUANTITA_STIMATE_PER_UNITA_SEMI *
                        RANDOM_TRIANGULAR(0.5, 1.5, 1.0);

                    IF IS_REUSABLE_SEED_PRODUCT(PROD.NOME_PRODOTTO) THEN
                        V_PRODUCTED := GREATEST(
                            V_PRODUCTED,
                            V_PLANTED_QUANTITY * RANDOM_NUMBER(1.8, 3.0)
                        );
                    END IF;

                    INSERT_AGRI_PRODUCTION(
                        TRUNC(P_DATA),
                        V_DATA_INIZIO,
                        P_CODICE_CELLA_IDR,
                        P_NOME_STRUTTURA,
                        PROD.NOME_PRODOTTO,
                        V_PRODUCTED
                    );
                END LOOP;
            END IF;

            END_CYCLE(
                V_DATA_INIZIO,
                P_CODICE_CELLA_IDR,
                P_NOME_STRUTTURA,
                TRUNC(P_DATA)
            );

            DBMS_OUTPUT.PUT_LINE(
                'Cell ' || P_NOME_STRUTTURA || ' ' || P_CODICE_CELLA_IDR ||
                ' has now ended the cycle'
            );
        END IF;
    END SIMULA_CELLA;

    PROCEDURE SIMULA_GIORNO(
        P_DATA IN DATE
    ) IS
    BEGIN
        FOR CELL IN (
            SELECT CODICE_CELLA_IDR, NOME_STRUTTURA
            FROM CELLA_IDROPONICA
            WHERE DATA_MONTAGGIO <= TRUNC(P_DATA)
            AND (DATA_SMONTAGGIO IS NULL OR DATA_SMONTAGGIO > TRUNC(P_DATA))
        ) LOOP
            SIMULA_CELLA(
                CELL.CODICE_CELLA_IDR,
                CELL.NOME_STRUTTURA,
                TRUNC(P_DATA)
            );
        END LOOP;
    END SIMULA_GIORNO;

    PROCEDURE RUN_SIMULAZIONE(
        P_DATA_FINE IN DATE DEFAULT TRUNC(SYSDATE)
    ) IS
        V_CURRENT_DAY DATE := GET_SIM_START_DAY;
    BEGIN
        IF V_CURRENT_DAY IS NULL THEN
            DBMS_OUTPUT.PUT_LINE('No simulation start day found');
            RETURN;
        END IF;

        WHILE V_CURRENT_DAY < TRUNC(P_DATA_FINE) LOOP
            SIMULA_GIORNO(V_CURRENT_DAY);
            V_CURRENT_DAY := V_CURRENT_DAY + 1;
        END LOOP;

        COMMIT;
    EXCEPTION
        WHEN OTHERS THEN
            ROLLBACK;
            RAISE;
    END RUN_SIMULAZIONE;
END SIMULATORE;
