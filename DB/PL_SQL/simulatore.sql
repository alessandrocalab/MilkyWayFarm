        P_CODICE_CELLA_IDR IN CICLO_COLT_UTILIZZA_SEMI_MISSIONE.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CICLO_COLT_UTILIZZA_SEMI_MISSIONE.NOME_STRUTTURA%TYPE,
        P_NOME_SEMI IN CICLO_COLT_UTILIZZA_SEMI_MISSIONE.NOME_SEMI%TYPE,
        P_DATA_ARRIVO_SEMI IN CICLO_COLT_UTILIZZA_SEMI_MISSIONE.DATA_ARRIVO_SEMI%TYPE,
        P_QUANTITA IN CICLO_COLT_UTILIZZA_SEMI_MISSIONE.QUANTITA%TYPE
    ) IS
    BEGIN
        INSERT INTO CICLO_COLT_UTILIZZA_SEMI_MISSIONE (
            DATA_INIZIO,
            CODICE_CELLA_IDR,
            NOME_STRUTTURA,
            NOME_SEMI,
            DATA_ARRIVO_SEMI,
            QUANTITA
        ) VALUES (
            P_DATA_INIZIO,
            P_CODICE_CELLA_IDR,
            P_NOME_STRUTTURA,
            P_NOME_SEMI,
            P_DATA_ARRIVO_SEMI,
            P_QUANTITA
        );
    END INSERT_CYCLE_USE_MISSION_SEED;

    PROCEDURE INSERT_CYCLE_USE_AGRI_PRODUCTION(
        P_DATA_INIZIO IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.DATA_INIZIO%TYPE,
        P_CODICE_CELLA_IDR IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.CODICE_CELLA_IDR%TYPE,
        P_NOME_STRUTTURA IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.NOME_STRUTTURA%TYPE,
        P_DATA_PRODUZIONE_AGRICOLA IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.DATA_PRODUZIONE_AGRICOLA%TYPE,
        P_DATA_INIZIO_CICLO_COLT IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.DATA_INIZIO_CICLO_COLTIVAZIONE%TYPE,
        P_CODICE_CELLA_IDR_PROD IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.CODICE_CELLA_IDR_PROD_AGR%TYPE,
        P_NOME_STRUTTURA_PROD IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.NOME_STRUTTURA_PROD_AGR%TYPE,
        P_NOME_PRODOTTO IN CICLO_COLT_UTILIZZA_PRODUZIONE_AGRICOLA.NOME_PRODOTTO%TYPE,
